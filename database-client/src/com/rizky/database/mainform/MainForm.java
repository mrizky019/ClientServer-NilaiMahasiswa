    public void fillComboBox(){
        String qry = "SELECT * FROM dosen";
        try {
            ps=con.prepareStatement(qry);
            rs=ps.executeQuery();
            while (rs.next()) {
                textMatkul.addItem(rs.getString("mataKuliah"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    public void isiNilai() throws RemoteException, NotBoundException{
        Registry registry = LocateRegistry.getRegistry("localhost", 6789);
        PersonService service = (PersonService) registry.lookup("service");
        Object textNamaMatkul = textMatkul.getSelectedItem().toString();
        Object textDosen = textNamaDosen.getText();
        String tm = textMatkul.getSelectedItem().toString();
        
        if (tm.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Pilih Kelas Dahulu!!!");
        }
        if (textMatkul.getSelectedIndex() == 0) {
            SwingUtilities.invokeLater(new Runnable() {
                @Override
                public void run() {
                    Form form = new Form(service, (String)textNamaMatkul);
                    form.getLabelMataKuliah().setText((String) textNamaMatkul);
                    form.getLabelNamaDosen().setText((String) textDosen);
                    form.setVisible(true);
                }
            });
        } else {
            SwingUtilities.invokeLater(new Runnable() {
                @Override
                public void run() {
                    Form form = new Form(service, (String)textNamaMatkul);
                    form.getLabelMataKuliah().setText((String) textNamaMatkul);
                    form.getLabelNamaDosen().setText((String) textDosen);
                    form.setVisible(true);
                }
            });
        }
        this.setVisible(false);   
    }


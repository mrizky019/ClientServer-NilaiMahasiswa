    private void textMatkulPopupMenuWillBecomeInvisible(javax.swing.event.PopupMenuEvent evt) {//GEN-FIRST:event_textMatkulPopupMenuWillBecomeInvisible
        // TODO add your handling code here:
        SwingUtilities.getRootPane(buttonIsiNilai).setDefaultButton(buttonIsiNilai);
        String tmp = (String)textMatkul.getSelectedItem();
        String qry = "SELECT * FROM dosen WHERE mataKuliah ='"+tmp+"'";
        try {
            ps=con.prepareStatement(qry);
            rs=ps.executeQuery();
            if (rs.next()) {
                String dosen = rs.getString("namaDosen");
                textNamaDosen.setText(dosen);
            }
        } catch (SQLException ex) {
            Logger.getLogger(MainForm.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_textMatkulPopupMenuWillBecomeInvisible

